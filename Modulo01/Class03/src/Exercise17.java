public class Exercise17 {
    public static void main(String[] args) {
        /*
         * Ejercicio 17
         * Escriba un programa que escriba en pantalla los números del 1 al 100 de 5 en
         * 5.
         */
        // Bucle que imprime los números del 1 al 100, aumentando de 5 en 5
        for (var i = 1; i <= 100; i += 5) {
            System.out.print(i + " "); // Imprime en la misma línea con espacio
        }
    }
}
